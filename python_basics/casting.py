#1 casting with integers
a = int(11)         #a will be 11
b = int(2.9)        #b will be 2
c = int("7")        #c will be 7


#2 casting with floats
x = float(20)       #x will be 20.0
y = float(2.9)      #y will be 2.9
z = float("30")     #z will be 30.0
w = float("40.22")  #w will be 40.22


#3 casting with strings
p = str("a1b2")     #p will be 'a1b2'
o = str(123)        #o will be '123'
v = str(92.83)      #v will be '92.83'


#4 checking the integers and strings
print(a)
print(b)
print(c)
print(p)
print(o)
print(v)


#5 checking the floats
print(x)
print(y)
print(z)
print(w)
