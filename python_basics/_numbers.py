#1 existing types
a = 2           # int
b = 3.4         # float
c = 5j          # complex


#2 checking the types
print(type(a))
print(type(b))
print(type(c))


#3 integers can be positive, negative, but must be whole
x = 0
y = 1234567890
z = -987654321

print(type(x))
print(type(y))
print(type(z))


#4 float means floating point, which contains one or more decimals
q = 6.67
w = 2.0
e = -37.37

print(type(q))
print(type(w))
print(type(e))


#5 floats also can be scientific with "e" included, which means the power of 10
f = 11e1
g = 56E7
h = -92.2e300

print(type(f))
print(type(g))
print(type(h))                                         